--------------------------------------------------------

  CREATE OR REPLACE VIEW "HARRIAGUE"."LTE_5195A_HOUR" ("PERIOD_START_TIME", "LTE_5195A_NUM", "LTE_5195A_DEN", "LTE_5195A") AS 
  SELECT TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') AS PERIOD_START_TIME,
SUM(LTE_5195A_NUM) AS LTE_5195A_NUM,
SUM(LTE_5195A_DEN) AS LTE_5195A_DEN,
ROUND (((DECODE (SUM(LTE_5195A_DEN), 0, 0, SUM(LTE_5195A_NUM) /
  SUM(LTE_5195A_DEN)) )*100),2) as LTE_5195A
FROM LTE_5195A
GROUP BY TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') , CO_NAME
;
