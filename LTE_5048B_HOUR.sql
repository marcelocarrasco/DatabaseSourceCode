--  DDL for View LTE_5048B_HOUR
--------------------------------------------------------

  CREATE OR REPLACE VIEW "HARRIAGUE"."LTE_5048B_HOUR" ("PERIOD_START_TIME", "CO_NAME", "LTE_5048B_NUM", "LTE_5048B_DEN", "LTE_5048B") AS 
  SELECT TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') AS PERIOD_START_TIME,  CO_NAME,
SUM(LTE_5048B_NUM) AS LTE_5048B_NUM,
SUM(LTE_5048B_DEN) AS LTE_5048B_DEN,
ROUND (((DECODE (SUM(LTE_5048B_DEN), 0, 0, SUM(LTE_5048B_NUM) /
  SUM(LTE_5048B_DEN)) )*100),2) as LTE_5048B
FROM LTE_5048B
GROUP BY TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') , CO_NAME
;
