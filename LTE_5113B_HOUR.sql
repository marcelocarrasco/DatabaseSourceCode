  CREATE OR REPLACE VIEW "HARRIAGUE"."LTE_5113B_HOUR" ("PERIOD_START_TIME", "CO_NAME", "LTE_5113B_NUM", "LTE_5113B_DEN", "LTE_5113B") AS 
  SELECT TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') AS PERIOD_START_TIME, CO_NAME,
SUM(LTE_5113B_NUM) AS LTE_5113B_NUM,
SUM(LTE_5113B_DEN) AS LTE_5113B_DEN,
ROUND (((DECODE (SUM(LTE_5113B_DEN), 0, 0, SUM(LTE_5113B_NUM) /
  SUM(LTE_5113B_DEN)) )*100),2) as LTE_5113B
FROM LTE_5113B
GROUP BY TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') , CO_NAME
;
