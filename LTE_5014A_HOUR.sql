  CREATE OR REPLACE VIEW "HARRIAGUE"."LTE_5014A_HOUR" ("PERIOD_START_TIME", "CO_NAME", "LTE_5014A_NUM", "LTE_5014A_DEN", "LTE_5014A") AS 
  SELECT TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') AS PERIOD_START_TIME, CO_NAME,
SUM(LTE_5014A_NUM) AS LTE_5014A_NUM, SUM(LTE_5014A_DEN) AS LTE_5014A_DEN,
ROUND (((DECODE (SUM(LTE_5014A_DEN), 0, 0, SUM(LTE_5014A_NUM) /
  SUM(LTE_5014A_DEN)) )*100),2) AS LTE_5014A
FROM LTE_5014A
GROUP BY TO_DATE(SUBSTR( TO_CHAR( PERIOD_START_TIME, 'DD/MM/RRRR HH24:MI'), 0, 13),'DD/MM/YYYY HH24:MI') , CO_NAME
;
