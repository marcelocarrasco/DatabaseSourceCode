--  DDL for View FLNS_3113A_MONTH
--------------------------------------------------------

  CREATE OR REPLACE VIEW "HARRIAGUE"."FLNS_3113A_MONTH" ("FECHA", "PERIOD_DURATION", "FINS_ID", "MME_NAME", "TA_ID", "FLNS_3113A") AS 
  SELECT TRUNC(FECHA , 'MONTH') AS FECHA, PERIOD_DURATION, FINS_ID,
       MME_NAME,TA_ID, ROUND (DECODE (AVG(PERIOD_DURATION*60), 0 ,0,
              AVG(FLNS_3113A) / AVG(PERIOD_DURATION*60) ) , 2) AS FLNS_3113A
FROM FLNS_3113A_HISTORICAL
GROUP BY  TRUNC(FECHA , 'MONTH'), PERIOD_DURATION, FINS_ID, TA_ID,MME_NAME
;
