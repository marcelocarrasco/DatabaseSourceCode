--------------------------------------------------------
--  DDL for View FLNS_3112A_CONS_WEEK
--------------------------------------------------------

  CREATE OR REPLACE VIEW "HARRIAGUE"."FLNS_3112A_CONS_WEEK" ("FECHA", "PERIOD_DURATION", "FINS_ID", "MME_NAME", "FLNS_3112A") AS 
  SELECT TRUNC(FECHA , 'DAY') AS FECHA, PERIOD_DURATION, FINS_ID,
       MME_NAME, ROUND (DECODE (AVG(PERIOD_DURATION*60), 0 ,0,
              SUM(FLNS_3112A) / AVG(PERIOD_DURATION*60) ) , 2) AS FLNS_3112A
FROM FLNS_3112A_HISTORICAL
GROUP BY  TRUNC(FECHA , 'DAY'), PERIOD_DURATION, FINS_ID, MME_NAME
;
